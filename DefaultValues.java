public class DefaultValues {
        int a;
        String  b;
        boolean c;
        char f;
        float d;
        double e;
        long g;
        short h;
        public static void main(String args[]){
            DefaultValues defaultValues=new DefaultValues();

            System.out.println(" Default values ");
            System.out.println("Integer : "+defaultValues.a);
            System.out.println("char : "+defaultValues.f);
            System.out.println("String : "+defaultValues.b);
            System.out.println("Boolean : "+defaultValues.c);
            System.out.println("float : "+defaultValues.d);
            System.out.println("double : "+defaultValues.e);
            System.out.println("long : "+defaultValues.g);
            System.out.println("short : "+defaultValues.h);

        }
    }


